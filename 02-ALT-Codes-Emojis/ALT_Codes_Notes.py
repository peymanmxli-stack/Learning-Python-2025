# ---------------------------------------------------------------------------
# 1. What Are ALT Codes?
# ---------------------------------------------------------------------------
# ALT codes are numeric codes used on Windows to type special characters.
# Hold ALT + type the number on the numeric keypad.


# ---------------------------------------------------------------------------
# 2. Requirements
# ---------------------------------------------------------------------------
# - Windows OS
# - Numeric keypad (Num Lock ON)
# - ALT key held while typing numbers


# ---------------------------------------------------------------------------
# 3. Common Uses
# ---------------------------------------------------------------------------
# - box drawing
# - separators
# - arrows
# - math symbols
# - currency symbols
# - accented letters


# ---------------------------------------------------------------------------
# 4. Printing Special Characters in Python
# ---------------------------------------------------------------------------

print("Box corner:", "╔")
print("Arrow:", "→")
print("Block:", "█")


# ---------------------------------------------------------------------------
# 5. Console Compatibility
# ---------------------------------------------------------------------------
# Modern terminals support Unicode.
# Older terminals may not render all symbols correctly.


